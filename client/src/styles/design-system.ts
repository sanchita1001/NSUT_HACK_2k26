// Helper functions
export const getRiskColor = (score: number) => {
    if (score >= 80) return colors.danger[600];
    if (score >= 60) return colors.warning[600];
    if (score >= 40) return colors.warning[400];
    return colors.success[600];
};

export const getStatusColor = (status: string) => {
    const statusColors: Record<string, string> = {
        'New': colors.primary[600],
        'Investigating': colors.warning[600],
        'Verified': colors.danger[600],
        'Dismissed': colors.neutral[500],
        'Closed': colors.success[600],
    };
    return statusColors[status] || colors.neutral[500];
};
